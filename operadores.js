// Os operadores vão nos permitir fazer operações, matematicas, de comparação e lógicas

// Aritiméticos

// 2+2 = 4
console.log(2+2);
console.log(2-2);
console.log(2*2);
console.log(8/2);

// Comparação
// Igual
console.log(2 == 2);
console.log(2 == 3);
// Diferente
console.log(2 != 3);
console.log(2 != 2);
// Igual escrito
console.log(2 === "2");
// Diferente escrito
console.log(2 !== "2");
// Maior que >
console.log(2>3);
console.log(2>1);
// Menor que <
console.log(2<3);
console.log(2<1);
// Maior e igual >=
console.log(2>=3);
console.log(2>=2);
// Menor e igual <=
console.log(2<=3);
console.log(2<=2);